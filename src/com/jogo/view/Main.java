    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (acharUsuario((String) comboBox.getSelectedItem()) == null) {
            JOptionPane.showMessageDialog(this, "Escolha um jogador ou crie um");
        } else {
            //PASSAR POR PARAMETRO O USUARIO LOGADO PARA A CLASSE DA CATEGORIA
            audio.stop();
            Categoria categoria = new Categoria(acharUsuario((String) comboBox.getSelectedItem()));
            categoria.setVisible(true);
            categoria.setLocationRelativeTo(null);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

