    public void playSound() {
        try {
            URL url = getClass().getResource("/sound/All Along The Watchtower.wav");
            audio = Applet.newAudioClip(url);
            audio.play();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    public void iniComboBox() {

        usuarios = new JogoController().ListarUsuario();
        for (Usuario u : usuarios) {
            comboBox.addItem(u.getNome());
        }
    }

    //RETORNAR OBJETO DE USUARIO, PARA PASSAR O PARAMETRO PARA OUTRA CLASSE.
    public Usuario acharUsuario(String nome) {
        for (Usuario u : usuarios) {
            if (u.getNome().equalsIgnoreCase(nome)) {
                return u;
            }
        }
        return null;
    }

